package com.flotandroidchart.flot.data;

public class RectOffset {
	public int left = 0;
	public int top = 0;
	public int right = 0;
	public int bottom = 0;

	public void reset(int _left, int _top, int _right, int _bottom) {
		this.left = _left;
		this.top = _top;
		this.right = _right;
		this.bottom = _bottom;
	}
}
